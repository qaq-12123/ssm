package cn.kgc.domain;


public class User {

  private Integer id;
  private String username;
  private String password;
  private String identity;
  private String phone;
  private String address;
  private String contact;
  private String emergency;
  private Integer integral;
  private Integer pjmoney;
  private double balance;
  private Integer rechargeId;
  private Integer ExpensecalendarId;
  private Integer rechargerecordId;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getUsername() {
    return username;
  }

  public void setUsername(String username) {
    this.username = username;
  }


  public String getPassword() {
    return password;
  }

  public void setPassword(String password) {
    this.password = password;
  }


  public String getIdentity() {
    return identity;
  }

  public void setIdentity(String identity) {
    this.identity = identity;
  }


  public String getPhone() {
    return phone;
  }

  public void setPhone(String phone) {
    this.phone = phone;
  }


  public String getAddress() {
    return address;
  }

  public void setAddress(String address) {
    this.address = address;
  }


  public String getContact() {
    return contact;
  }

  public void setContact(String contact) {
    this.contact = contact;
  }


  public String getEmergency() {
    return emergency;
  }

  public void setEmergency(String emergency) {
    this.emergency = emergency;
  }


  public Integer getIntegral() {
    return integral;
  }

  public void setIntegral(Integer integral) {
    this.integral = integral;
  }


  public Integer getPjmoney() {
    return pjmoney;
  }

  public void setPjmoney(Integer pjmoney) {
    this.pjmoney = pjmoney;
  }


  public double getBalance() {
    return balance;
  }

  public void setBalance(double balance) {
    this.balance = balance;
  }


  public Integer getRechargeId() {
    return rechargeId;
  }

  public void setRechargeId(Integer rechargeId) {
    this.rechargeId = rechargeId;
  }


  public Integer getExpensecalendarId() {
    return ExpensecalendarId;
  }

  public void setExpensecalendarId(Integer ExpensecalendarId) {
    this.ExpensecalendarId = ExpensecalendarId;
  }


  public Integer getRechargerecordId() {
    return rechargerecordId;
  }

  public void setRechargerecordId(Integer rechargerecordId) {
    this.rechargerecordId = rechargerecordId;
  }

}
