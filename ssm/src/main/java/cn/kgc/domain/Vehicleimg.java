package cn.kgc.domain;


public class Vehicleimg {

  private int id;
  private int vid;
  private String vimg;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getVid() {
    return vid;
  }

  public void setVid(int vid) {
    this.vid = vid;
  }


  public String getVimg() {
    return vimg;
  }

  public void setVimg(String vimg) {
    this.vimg = vimg;
  }

}
