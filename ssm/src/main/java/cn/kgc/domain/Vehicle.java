package cn.kgc.domain;


public class Vehicle {

  private Integer id;
  private String brand;
  private String type;
  private String driveType;
  private Integer seat;
  private Integer rentMoney;
  private String quality;
  private String license;
  private String img;
  private String owner;
  private String safetyPeril;


  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getBrand() {
    return brand;
  }

  public void setBrand(String brand) {
    this.brand = brand;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public String getDriveType() {
    return driveType;
  }

  public void setDriveType(String driveType) {
    this.driveType = driveType;
  }


  public Integer getSeat() {
    return seat;
  }

  public void setSeat(Integer seat) {
    this.seat = seat;
  }


  public Integer getRentMoney() {
    return rentMoney;
  }

  public void setRentMoney(Integer rentMoney) {
    this.rentMoney = rentMoney;
  }


  public String getQuality() {
    return quality;
  }

  public void setQuality(String quality) {
    this.quality = quality;
  }


  public String getLicense() {
    return license;
  }

  public void setLicense(String license) {
    this.license = license;
  }


  public String getImg() {
    return img;
  }

  public void setImg(String img) {
    this.img = img;
  }


  public String getOwner() {
    return owner;
  }

  public void setOwner(String owner) {
    this.owner = owner;
  }


  public String getSafetyPeril() {
    return safetyPeril;
  }

  public void setSafetyPeril(String safetyPeril) {
    this.safetyPeril = safetyPeril;
  }

}
